#include "3-calc.h"
/**
 * op_add-sum
 * @a:1st no
 * @b:2nd no
 * Return:result
 */
int op_add(int a, int b)
{
	return (a + b);
}
/**
 * op_sub-diff
 * @a:1st no
 * @b:2n no
 * Return:diff
 */
int op_sub(int a, int b)
{
	return (a - b);
}
/**
 * op_mul-product
 * @a:1st
 * @b:2nd
 * Return:product
 */
int op_mul(int a, int b)
{
	return (a * b);
}
/**
 * op_div-division
 * @a:1st
 * @b:2nd
 * Return:division
 */
int op_div(int a, int b)
{
	return (a / b);
}
/**
 * op_mod-remainder
 * @a:1st
 * @b:2nd
 * Return:remainder
 */
int op_mod(int a, int b)
{
	return (a % b);
}




